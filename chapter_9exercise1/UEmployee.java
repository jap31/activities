package try3.hello.src.chapter_9exercise1;

public class UEmployee {

    private String name;
    private float salary;

    public UEmployee(String name, float salary){

        this.name=name;
        this.salary=salary;
    }
    
    
    public String getName() {
        return name;
    }
  
    public float getSalary() {
        return salary;
    }


}
